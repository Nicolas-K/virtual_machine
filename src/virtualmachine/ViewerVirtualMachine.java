    private void openFileButtonActionPerformed(java.awt.event.ActionEvent evt) {
        int approve, countCommands = 0, rowCount = 0, columnCount = 0, parametersCount;
        File codeFile;

        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setFileSelectionMode(JFileChooser.FILES_ONLY);
        approve = fileChooser.showOpenDialog(ViewerVirtualMachine.this);

        if (approve == JFileChooser.APPROVE_OPTION) {
            codeFile = fileChooser.getSelectedFile();

            try {
                listCommands = virtualMachineController.openCodeFile(codeFile.getAbsolutePath());

                while (countCommands < listCommands.size()) {
                    codeTable.getModel().setValueAt(listCommands.get(countCommands).getCommandLine(),
                            rowCount, columnCount);

                    codeTable.getModel().setValueAt(listCommands.get(countCommands).getCommandName(),
                            rowCount, columnCount + 1);

                    parametersCount = listCommands.get(countCommands).getParameters().size();

                    if (parametersCount != 0) {
                        if (parametersCount == 1) {
                            codeTable.getModel().setValueAt(listCommands.get(countCommands).getParameters().get(0),
                                    rowCount, columnCount + 2);
                        } else {
                            if (parametersCount == 2) {
                                codeTable.getModel().setValueAt(listCommands.get(countCommands).getParameters().get(0),
                                        rowCount, columnCount + 2);

                                codeTable.getModel().setValueAt(listCommands.get(countCommands).getParameters().get(1),
                                        rowCount, columnCount + 3);
                            }
                        }
                    }

                    rowCount++;
                    countCommands++;
                }

                toRestart = true;

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
        }
    }

    private void executeCode() {
        int nextPC = 0, input = 0;

        if (stepByStepAction == true) {
            if (toRestart == true) {
                restartVirtualMachine();
            }

            stepByStepAction = true;
            refreshCurrentPC(PC);

            if (listCommands.get(PC).getCommandName().equals("RD")) {
                inputTextField.setText(JOptionPane.showInputDialog("Digite o valor de entrada:"));
                input = Integer.parseInt(inputTextField.getText());
            }

            nextPC = virtualMachineController.executeCode(PC, input);

            if (listCommands.get(PC).getCommandName().equals("PRN")) {
                outputTextField.setText("");
                int print = virtualMachineController.getPrintValue();
                outputTextField.setText(Integer.toString(print));
            }

            PC = nextPC;

            if (listCommands.get(PC).getCommandName().equals("HLT")) {
                virtualMachineController.endExecution();
                hasEnded = true;
                JOptionPane.showMessageDialog(null, "Encerrada a execução do programa");
            }

        } else {
            if (toRestart == true) {
                restartVirtualMachine();
            }

            while (!listCommands.get(PC).getCommandName().equals("HLT")) {
                refreshCurrentPC(PC);

                if (listCommands.get(PC).getCommandName().equals("RD")) {
                    inputTextField.setText(JOptionPane.showInputDialog("Digite o valor de entrada:"));
                    input = Integer.parseInt(inputTextField.getText());
                }

                nextPC = virtualMachineController.executeCode(PC, input);

                if (nextPC == -1) {
                    virtualMachineController.endExecution();
                    hasEnded = true;
                    JOptionPane.showMessageDialog(null, "Encerrada a execução do programa devido a erro de execução");
                    break;
                }

                if (listCommands.get(PC).getCommandName().equals("PRN")) {
                    outputTextField.setText("");
                    int print = virtualMachineController.getPrintValue();
                    outputTextField.setText(Integer.toString(print));
                }

                PC = nextPC;
            }

            if (listCommands.get(PC).getCommandName().equals("HLT")) {
                virtualMachineController.endExecution();
                hasEnded = true;
                JOptionPane.showMessageDialog(null, "Encerrada a execução do programa");
            }
        }
    }

    private void executeButtonActionPerfomed(java.awt.event.ActionEvent evt) {
        stepByStepAction = false;
        executeCode();
    }

    private void stepByStepButtonActionPerformed(java.awt.event.ActionEvent evt) {
        stepByStepAction = !stepByStepAction;
    }

    private void continueButtonActionPerformed(java.awt.event.ActionEvent evt) {
        if (!hasEnded) {
            executeCode();
        }
    }

    private void restartVirtualMachine() {
        PC = 0;
        toRestart = false;
        hasEnded = false;
        stepByStepActive = false;
        stepByStepAction = false;
    }

    private void refreshCurrentPC(int currentPC) {
        currentPCTextField.setText(Integer.toString(currentPC));
    }
